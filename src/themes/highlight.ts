/**
 * Get highlight theme CSS by name
 */
export function getHighlightThemeCSS(name: string): string {
  switch (name.toLowerCase()) {
    case 'github':
    case 'github-light':
      return highlightGithubTheme;
    case 'github-dark':
      return highlightGithubDarkTheme;
    case 'vscode-light':
      return highlightVSCodeLightTheme;
    case 'vscode-dark':
      return highlightVSCodeDarkTheme;
    default:
      return highlightGithubTheme;
  }
}

export default {
  highlightGithubTheme,
  highlightGithubDarkTheme,
  highlightVSCodeLightTheme,
  highlightVSCodeDarkTheme,
  getHighlightThemeCSS,
};
